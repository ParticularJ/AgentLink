def get_stock_sector(stock_code: str, stock_name: str = '') -> str:
    """
    根据股票代码和名称判断所属板块
    
    Args:
        stock_code: 股票代码
        stock_name: 股票名称（用于判断ST）
    
    Returns:
        板块名称
    """
    # 根据代码前缀判断
    if stock_code.startswith('688'):
        return '科创板'
    elif stock_code.startswith('300'):
        return '创业板'
    elif stock_code.startswith('8'):
        return '北交所'
    
    # 根据名称判断ST
    if 'ST' in stock_name or '*ST' in stock_name:
        return 'ST板块'
    
    # 根据股票池判断
    for sector, codes in SECTOR_STOCK_POOLS.items():
        if stock_code in codes:
            return sector
    
    # 默认返回
    return 'default'


def get_sector_by_code_prefix(stock_code: str) -> str:
    """
    仅根据代码前缀判断板块
    """
    if stock_code.startswith('688'):
        return '科创板'
    elif stock_code.startswith('300'):
        return '创业板'
    elif stock_code.startswith('8'):
        return '北交所'
    elif stock_code.startswith('60'):
        return '沪市主板'
    elif stock_code.startswith('00'):
        return '深市主板'
    elif stock_code.startswith('30'):
        return '创业板'
    else:
        return 'default'
